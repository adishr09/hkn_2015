<p>Eta Kappa Nu has been dedicated to encouraging and recognizing excellence in Electrical and Computer Engineering. Not only does it honor scholarship, but also instills character, attitude and a sense of 

community in its members that would help mould them into the future leaders in their professions. This inspired us at Bharati Vidyapeeth’s College of Engineering to form the first honor society in India, the 

Lambda Eta chapter.

 <br/><br/>
When I got the chance to form the Eta Kappa Nu chapter at our college, I saw it as a unique opportunity for students at my college to get access to a variety of service programs, leadership training and an 

invaluable network for academic tutoring, career counseling, grad school etc. It motivated me to get the required documentation and initiate the chapter at our college. Thanks to the encouragement of Prof 

Rahul Bahl, the efforts of Prof. Stephen Goodnick and Fern Katronetsky, we had our chapter initiation on February 10th, 2012. In our first year itself, we organized a fest, a number of community initiatives, 

tutoring classes, research paper presentations, coding competitions, workshops etc. This resulted in us winning the outstanding chapter award in the first year itself.

 
<br/><br/>
At Lambda Eta, we aim to instill each member with ingenuity, imagination, practical innovation, and problem solving ability. We have a motivated body of Officers, who’ve won the Outstanding Chapter Award 

